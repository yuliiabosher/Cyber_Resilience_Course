def add_numbers(num1, num2):
  sum = num1 + num2
  return sum
def multiply_numbers(num1,num2): 
  mult=num1*num2
  return mult
def subtract_numbers(num1,num2):
  sub=num1-num2
  return sub	
def divide_numbers(num1,num2):
  if num2!=0:
    div=num1/num2
    return div
  else:
     return "cannot divide by zero"
